// Functions For Fun...Expressions vs Declarations

// Expression
var question = function () { 
    console.log('Whoa...a Jackolantern!');
};

// Expression
var jackolantern = function () { 
    console.log('I am not a Jackolantern...');
};

// Declaration
var lewis = () => { 
    console.log('My name....is Leeewis...');
}; 

// Declaration
var dang = () => {
    console.log('Did that thing just talk?....');
};

question();
jackolantern(); 
lewis(); 
dang();
